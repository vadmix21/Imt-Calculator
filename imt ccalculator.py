while True:
    try:
        
        вес = float(input("Введите вес в кг (от 20 до 300): "))
        if вес < 20 or вес > 300:
            print("Ошибка: вес должен быть от 20 до 300 кг!")
            continue

        
        рост = float(input("Введите рост в метрах (например 1.68, от 0.5 до 2.5): "))
        if рост < 0.5 or рост > 2.5:
            print("Ошибка: рост должен быть от 0.5 до 2.5 метра!")
            continue

        
        if вес <= 0 or рост <= 0:
            print("Ошибка: вес и рост должны быть положительными!")
            continue

        
        имт = вес / (рост ** 2)
        
       
        if имт < 18.5:  
            категория = "Недостаток веса"  
        elif имт < 25:  
            категория = "Норма"  
        elif имт < 30:  
            категория = "Избыточный вес"  
        else:  
            категория = "Ожирение"
        
        
        print("\nРезультаты:")
        print(f"Ваш вес: {вес} кг")
        print(f"Ваш рост: {рост} м")
        print(f"Индекс массы тела (ИМТ): {round(имт, 2)}")
        print(f"Категория: {категория}")
        
       
        if категория == "Недостаток веса":
            print("Рекомендация: увеличьте калорийность питания.")
        elif категория == "Ожирение":
            print("Рекомендация: обратитесь к диетологу для консультации.")
        elif категория == "Избыточный вес":
            print("Рекомендация: увеличьте физическую активность.")
        else:
            print("Рекомендация: поддерживайте текущий образ жизни!")
        
        break  

    except ValueError:
        print("Ошибка! Введите числовое значение.")